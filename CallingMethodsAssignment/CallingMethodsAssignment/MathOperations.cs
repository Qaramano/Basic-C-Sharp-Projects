// MathOperations.cs
namespace MyMathApp
{
    public class MathOperations
    {
        // Method for addition
        public int Add(int number)
        {
            // Add 4 to the input number
            return number + 4;
        }

        // Method for subtraction
        public int Subtract(int number)
        {
            // Subtract 3 from the input number
            return number - 3;
        }

        // Method for division
        public int Divide(int number)
        {
            // Divide the input number by 2
            return number / 2;
        }
    }
}
